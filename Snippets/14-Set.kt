fun main(){
    val num = setOf(1 ,2, 3, 4, 5, 6, 7, 8)
 
    println("The number of element in the set is: "+num.count())
    println("The maximum element in the set is: "+num.max())
    println("The minimum element in the set is: "+num.min())
